# 05 Weather Activity Suggestion: Suggest an activity based on the weather (e.g., Sunny - Go for a walk, Rainy - Read a book, Snowy - Build a snowman).

weather = input("Tell Me How's The Weather: ")

if weather == "Sunny":
    print("Go for a walk")
elif weather == "Rainy":
    print("Read a Book")
elif weather == "Snowy":
    print("Build a snowman")